import UIKit

extension UIColor {
    class func paperGray() -> UIColor {
        return UIColor(red: 243/255, green: 243/255, blue: 243/255, alpha: 1)
    }
    
    class func bloodRed() -> UIColor {
        return UIColor(red: 255/255, green: 54/255, blue: 85/255, alpha: 1)
    }
    
    class func indigoBlue() -> UIColor {
        return UIColor(red: 34/255, green: 51/255, blue: 79/255, alpha: 1)
    }
    
    class func facebook() -> UIColor {
        return UIColor(red: 59/255, green: 89/255, blue: 152/255, alpha: 1)
    }
}